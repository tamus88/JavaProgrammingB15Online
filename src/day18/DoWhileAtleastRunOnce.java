package day18;

public class DoWhileAtleastRunOnce {

    public static void main(String[] args) {

        int x = 1 ;
        do{
            System.out.println("Keep Going ");
        }while (x<5) ;

        System.out.println("The End");


        while (x<5){

            System.out.println("Keep Going In While");
        }







    }
}
