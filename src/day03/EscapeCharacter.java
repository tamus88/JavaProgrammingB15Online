package day03;

public class EscapeCharacter {

    public static void main(String[] args) {

     System.out.println("Hello \\ world ")  ;    //  "\\ means \ "; " \\\\ means \\ " ; backslash

        System.out.println("I like the book \" Java \" awesome  ");  // Double quotation marks

        System.out.println("The movie name is : 'Lord of the rings' "); // Single quotation marks

        System.out.println("Hello\tWorld"); // Tab format

        System.out.println("Hello\nB15");

        System.out.print("This is regular print "); // "This is dog"; "This is cat"; etc

        System.out.println("This is println"); /* "This is dog";
                                                  "This is cat"  */



    }
}
